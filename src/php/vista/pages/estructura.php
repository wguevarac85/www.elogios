	<section class="compania__elogios">
		<h1 class="compania__titulo">Nuestra Compañia</h1>
		<p class="compania__parrafo">
		<strong>ELOGIOS</strong> es una institución sin animo de lucro que brinda sus servicios  desde hace mas de 25 años en diferentes areas; <em>salud mental, salud ocupacional, educacion y laboratorio clinico</em>, la caracteristica de  los  programas es que tienen  en comun  el dirigir o  guiar a los  usuarios en la busqueda de un  mayor grado de bienestar.
		</p>
		<p class="compania__parrafo">
		Contamos con 4 unidades operacionales de atención e  intervención:
		<strong>Elogios salud mental:</strong> programa encaminado en la habilitación de pacientes con discapacidad intelectual  y trastornos mentales colaterales,  brindando  un tratamiento integral en las areas de <b>Medcina, Psiquiatria, psicologia, fonoaudiologia, fisioterapia, terapia ocupacional</b> entre otros en busca de alcanzar el mayor bienestar y funcionalidad de acuerdo a sus niveles de discapacidad de los usuario.
		</p>
		<p class="compania__parrafo">
		<strong>Elogios Formación:</strong>  Aprendizaje encaminado en el desarrollo humano y el trabajo, dirigido a  personas  con discapacidad intelectual  que buscan aportar un sentido y significado a su existencia a traves de "talleres para la vida", y destrezas practicas aplicables en la cotidianida.
		</p>
		<p class="compania__parrafo">
		<strong>Elogios Salud laboral:</strong> (somos) una <strong>IPS</strong> lider en la prestación de servicios en el area de seguridad y salud en el trabajo, nuestro labor (fin) se enfoca en el area de <strong>Medicina preventiva, general y del trabajo,</strong>  en donde ofrecemos a nuestros clientes el asesoramiento, desarrollo y ejecucion de diversos  examenes medico laborales, examenes complementarios (paraclinicos), consulta medica general, psicologica y otros, garantizando conceptos medicos consistentes que fundamentan la toma de decisiones para mejorar la calidad de vida de las personas en general, y la construccion de programas de seguridad y salud en el trabajo en el marco de las acciones del sistema de seguridad y salud en el trabajo (SG-SST).   Por otra parte, ofrecemos asesorias en el area de higiene y seguridad industrial que complementen su SGSST. (RESUMIR)
		</p>
		<p class="compania__parrafo">
		<strong>Elogios Laboratorio Clinico:</strong>  Somos expertos  y estamos habilitados para realizar diagnosticos clinicos , desarrollar los analisis que contrituyen al estudio para ofrecer a nuestros usuarios examenes generales y especializados asegurando un diagnostico confiable y oportuno.
		</p>
